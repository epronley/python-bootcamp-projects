# _________________________________SCOREBOARD CLASS______________________________ #
from turtle import Turtle


class Scoreboard(Turtle):

    # _________________________________INITIALIZE______________________________ #
    def __init__(self):
        super().__init__()
        self.color("white")
        self.penup()
        self.hideturtle()
        self.r_score = 0
        self.l_score = 0
        self.update_scoreboard()

    # _________________________________UPDATE SCOREBOARD______________________________ #
    def update_scoreboard(self):
        self.clear()
        self.goto(-100, 200)
        self.write(self.l_score, align="center", font=("Courier", 80, "normal"))
        self.goto(100, 200)
        self.write(self.r_score, align="center", font=("Courier", 80, "normal"))

    # _________________________________DETERMINE WHO GETS THE POINT______________________________ #
    def l_point(self):
        self.l_score += 1
        self.update_scoreboard()

    def r_point(self):
        self.r_score += 1
        self.update_scoreboard()
